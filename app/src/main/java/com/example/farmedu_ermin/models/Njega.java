package com.example.farmedu_ermin.models;

public class Njega {

    private String dlaka;
    private String kupanje;
    private String zubi;
    private String nokti;
    private String linjanje;

    public Njega(String dlaka, String kupanje,
                 String zubi, String nokti, String linjanje) {

        this.dlaka = dlaka;
        this.kupanje = kupanje;
        this.zubi = zubi;
        this.nokti = nokti;
        this.linjanje = linjanje;
    }
}